nums = [3, 41, 12, 9, 74, 15]
print(len(nums)) #len() returns the number of items in a list
print(max(nums)) #max() returns the largest item in a list
print(min(nums)) #min() returns the smallest item in a list
print(sum(nums)) #sum() returns the sum of all items in a list
print(sum(nums)/len(nums)) #average of the list